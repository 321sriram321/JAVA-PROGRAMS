class twodarray{
    public static void main(String args[]){
        int[][] a={{4,5},{4,5,6}};
         int n=a.length;
        for(int i=0;i<n;i++){
            for(int j=0;j<a[i].length;j++){
                System.out.print(a[i][j]);
            }
        System.out.println();
        }

    }
}